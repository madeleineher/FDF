#include "libft.h"

int		ft_only_fdf(char c)
{
	if (c == ',' || c == '-' || c == 'F' || c == 'X' || c == 'x' || c == 'f' 
			|| c == ' ' || c == '\n' ||  (c > 47 && c < 58))
		return (1);
	return (0);
}
